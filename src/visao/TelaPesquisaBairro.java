    private void jTBairroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTBairroMouseClicked
        if (jTBairro.getSelectedRow() != -1) {
            Cod = (jTBairro.getValueAt(jTBairro.getSelectedRow(), 0).toString());
            Bairro = (jTBairro.getValueAt(jTBairro.getSelectedRow(), 1).toString());
            int i = JOptionPane.showConfirmDialog(this, "Confirma a Bairro: " + Bairro);
            if (i == 0) {
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTBairroMouseClicked

