    public void LimparCampos() {
        txtConsulta.setText("");
    }

    public void Consultar() {
        if (jRCodigo.isSelected()) {
            MostrarBairroCodigo(Integer.parseInt(txtConsulta.getText()));
            LimparCampos();
        } else if (jRNome.isSelected()) {
            MostrarBairroNome(txtConsulta.getText());
            LimparCampos();
        }
    }

